    # ---- SCHEDULED REPORTS ----
    @staticmethod
    def schedule_report(
        company_id: str,
        report_type: str,
        frequency: str = "weekly",
        recipient_emails: List[str] = None,
        config: Dict = None,
    ) -> Dict[str, Any]:
        """Schedule an automated report."""
        sched_id = f"SCHED-{uuid.uuid4().hex[:8].upper()}"
        freq_map = {"daily": 1, "weekly": 7, "monthly": 30, "quarterly": 90}
        days = freq_map.get(frequency, 7)
        next_run = (datetime.now() + timedelta(days=days)).isoformat()

        conn = sqlite3.connect(DB_PATH)
        try:
            conn.execute("""
                INSERT INTO scheduled_reports (id, company_id, report_type, frequency, recipient_emails, next_run, config_json)
                VALUES (?, ?, ?, ?, ?, ?, ?)
            """, (
                sched_id, company_id, report_type, frequency,
                json.dumps(recipient_emails or []), next_run, json.dumps(config or {}),
            ))
            conn.commit()
            return {
                "id": sched_id,
                "status": "scheduled",
                "frequency": frequency,
                "next_run": next_run,
                "recipients": recipient_emails,
            }
        finally:
            conn.close()

    @staticmethod
    def list_scheduled_reports(company_id: str) -> List[Dict]:
        """List all scheduled reports."""
        conn = sqlite3.connect(DB_PATH)
        conn.row_factory = sqlite3.Row
        try:
            return [dict(r) for r in conn.execute(
                "SELECT * FROM scheduled_reports WHERE company_id=? ORDER BY next_run",
                (company_id,)
            ).fetchall()]
        finally:
            conn.close()

