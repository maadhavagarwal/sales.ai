    @staticmethod
    def generate_segment_documents(company_id: str, segment_id: str, doc_type: str, created_by: int = 1) -> Dict:
        """Generate documents for all members of a segment."""
        from app.engines.segment_engine import SegmentEngine
        members = SegmentEngine.get_segment_for_documents(segment_id)
        if not members:
            return {"status": "no_members", "message": "Segment has no members"}

        generated = []
        for member in members:
            result = DocumentEngine.generate_document(
                company_id=company_id,
                doc_type=doc_type,
                title=f"{doc_type.replace('_', ' ').title()} - {member.get('name', member.get('customer_id', 'Customer'))}",
                data={"customer": member},
                segment_id=segment_id,
                created_by=created_by,
            )
            generated.append({"customer": member.get("customer_id"), "doc_id": result.get("id")})

        return {
            "status": "success",
            "segment_id": segment_id,
            "documents_generated": len(generated),
            "documents": generated,
        }
