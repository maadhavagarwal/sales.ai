    # ---- DOCUMENT MANAGEMENT ----
    @staticmethod
    def list_documents(company_id: str, doc_type: str = None, limit: int = 50) -> List[Dict]:
        """List all documents for a company."""
        conn = sqlite3.connect(DB_PATH)
        conn.row_factory = sqlite3.Row
        try:
            query = "SELECT * FROM documents WHERE company_id=?"
            params = [company_id]
            if doc_type:
                query += " AND doc_type=?"
                params.append(doc_type)
            query += " ORDER BY created_at DESC LIMIT ?"
            params.append(limit)
            return [dict(r) for r in conn.execute(query, params).fetchall()]
        finally:
            conn.close()

    @staticmethod
    def get_document(doc_id: str, company_id: str) -> Dict[str, Any]:
        """Get document details."""
        conn = sqlite3.connect(DB_PATH)
        conn.row_factory = sqlite3.Row
        try:
            doc = conn.execute(
                "SELECT * FROM documents WHERE id=? AND company_id=?", (doc_id, company_id)
            ).fetchone()
            if not doc:
                return {"error": "Document not found"}

            versions = conn.execute(
                "SELECT * FROM document_versions WHERE document_id=? ORDER BY version DESC",
                (doc_id,)
            ).fetchall()

            return {**dict(doc), "versions": [dict(v) for v in versions]}
        finally:
            conn.close()

    @staticmethod
    def delete_document(doc_id: str, company_id: str) -> Dict[str, Any]:
        """Archive a document."""
        conn = sqlite3.connect(DB_PATH)
        try:
            conn.execute(
                "UPDATE documents SET status='archived', updated_at=CURRENT_TIMESTAMP WHERE id=? AND company_id=?",
                (doc_id, company_id)
            )
            conn.commit()
            return {"status": "archived", "document_id": doc_id}
        finally:
            conn.close()

