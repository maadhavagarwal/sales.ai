from app.engines.llm_engine import ask_llm
from app.core.strict_mode import require_real_services


def generate_detailed_strategic_plan(analytics, insights, strategy, pricing_opt=None):
    """
    Asks the AI to generate a comprehensive, multi-page level strategic business plan.
    """
    context = f"""
        You are a Senior Strategic Business Partner at an elite tier-1 consulting firm (e.g., McKinsey/Bain).
        Based on the following data points, generate a highly detailed, professional, and actionable Strategic Business Plan.
        Do NOT use cliché language. Use precise, data-driven terminology (e.g., EBITDA, LTV, TAM, CAC, Elasticity).

        DATA SUMMARY:
        {analytics}

        INITIAL INSIGHTS:
        {insights}

        PROPOSED STRATEGIES:
        {strategy}

        RL PRICING OPTIMIZATION RESULTS:
        {pricing_opt}

        The report MUST be structured with the following sections:
        1. EXECUTIVE SUMMARY (High-level data-driven overview)
        2. MARKET PERFORMANCE ANALYSIS (Deep dive into revenue concentration and regional data)
        3. PRODUCT & INVENTORY STRATEGY (Optimization of top offerings, cannibalization risks)
        4. PRICING OPTIMIZATION STRATEGY (Explain the RL model's recommended price adjustment elasticity)
        5. FINANCIAL & CAPITAL ALLOCATION PLAN (Strict EBITDA margin targets, expense reduction)
        6. MARKETING & ACQUISITION STRATEGY (Omnichannel spend optimization, CAC reduction)
        7. OPERATIONAL EFFICIENCY (Margin improvement and cost control dynamics)
        8. 12-MONTH STRATEGIC ROADMAP (Phased implementation plan: Q1-Q4)
        9. RISK ASSESSMENT & MITIGATION (Macro/Micro risk factors)

        Format the response in clear Markdown with professional headings. Use a formal, objective, and authoritative tone.
    """

    try:
        plan = ask_llm(context)
        if "unavailable" in plan.lower() or "ollama" in plan.lower():
            return _generate_fallback_plan(analytics, insights, strategy, pricing_opt)
        return plan
    except:
        return _generate_fallback_plan(analytics, insights, strategy, pricing_opt)


